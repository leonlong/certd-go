func fetchCertificateFromURL(url, fallbackCN, fallbackPEM string) *x509.Certificate {
	client := &http.Client{Timeout: 5 * time.Second}

	resp, err := client.Get(url)
	if err == nil && resp.StatusCode == 200 {
		defer resp.Body.Close()
		buf := make([]byte, 4096)
		n, _ := resp.Body.Read(buf)
		pemData := buf[:n]

		block, _ := pem.Decode(pemData)
		if block != nil {
			cert, err := x509.ParseCertificate(block.Bytes)
			if err == nil {
				return cert
			}
		}
	}

	block, _ := pem.Decode([]byte(fallbackPEM))
	if block == nil {
		return nil
	}

	cert, err := x509.ParseCertificate(block.Bytes)
	if err != nil {
		return nil
	}

	return cert
}

func GetIssuerName(cert *x509.Certificate) string {
	if cert == nil {
		return ""
	}

	parts := []string{}
	if cert.Issuer.CommonName != "" {
		parts = append(parts, cert.Issuer.CommonName)
	}
	if cert.Issuer.Organization != nil && len(cert.Issuer.Organization) > 0 {
		parts = append(parts, cert.Issuer.Organization[0])
	}
	if cert.Issuer.OrganizationalUnit != nil && len(cert.Issuer.OrganizationalUnit) > 0 {
		parts = append(parts, cert.Issuer.OrganizationalUnit[0])
	}

	return strings.Join(parts, " ")
}
